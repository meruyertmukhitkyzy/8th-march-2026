const slider = document.querySelector('.slider');
const items = document.querySelectorAll('.slider .item');

const popup = document.getElementById('predictionPopup');
const popupImage = document.getElementById('popupImage');
const popupCardTitle = document.getElementById('popupCardTitle');
const popupPoemTitle = document.getElementById('popupPoemTitle');
const popupDescription = document.getElementById('popupDescription');
const popupClose = document.querySelector('.popup__close');
const popupOverlay = document.querySelector('.popup__overlay');
const popupContent = document.querySelector('.popup__content');

let selectedItem = null;
let currentRotation = 0;

function stopAndFocusCard(item) {
    const quantity = Number(slider.style.getPropertyValue('--quantity')) || items.length;
    const position = Number(item.style.getPropertyValue('--position'));
    const anglePerCard = 360 / quantity;

    currentRotation = -((position - 1) * anglePerCard);

    slider.classList.add('is-stopped', 'has-selection');
    items.forEach(card => card.classList.remove('active'));
    item.classList.add('active');

    slider.style.transform = `perspective(1000px) rotateX(-16deg) rotateY(${currentRotation}deg)`;
}

function openPopup(item) {
    const cardName = item.dataset.card || 'Prediction';
    const image = item.dataset.image || item.querySelector('img')?.src || '';
    const alt = item.querySelector('img')?.alt || cardName;

    const reading = cardReadings[cardName] || {
        poemTitle: 'Untitled',
        poem: 'No poem added yet.'
    };

    popupCardTitle.textContent = cardName;
    popupPoemTitle.textContent = reading.poemTitle;
    popupDescription.textContent = reading.poem;
    popupImage.src = image;
    popupImage.alt = alt;

    popup.classList.add('is-open');
    popup.setAttribute('aria-hidden', 'false');
    document.body.classList.add('popup-open');

        requestAnimationFrame(() => {
        popupContent.scrollTop = 0;
    });
}

function resumeCarousel() {
    selectedItem = null;
    items.forEach(card => card.classList.remove('active'));
    slider.classList.remove('is-stopped', 'has-selection');
    slider.style.transform = '';
}

function closePopup() {
    popup.classList.remove('is-open');
    popup.setAttribute('aria-hidden', 'true');
    document.body.classList.remove('popup-open');
    popupContent.scrollTop = 0;
    resumeCarousel();
}

items.forEach(item => {
    item.addEventListener('click', (e) => {
        e.stopPropagation();

        if (selectedItem !== item) {
            selectedItem = item;
            stopAndFocusCard(item);
            return;
        }

        openPopup(item);
    });
});

popupClose.addEventListener('click', closePopup);
popupOverlay.addEventListener('click', closePopup);

document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape' && popup.classList.contains('is-open')) {
        closePopup();
    }
});

document.addEventListener('click', (e) => {
    const clickedCard = e.target.closest('.slider .item');
    const clickedInsidePopup = e.target.closest('.popup__card');

    if (clickedCard) return;
    if (clickedInsidePopup) return;

    if (popup.classList.contains('is-open')) {
        closePopup();
        return;
    }

    if (selectedItem) {
        resumeCarousel();
    }
});